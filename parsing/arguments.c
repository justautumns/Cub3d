#include "../cub3d.h"

int	arg_check(char *str)
{
	int	i;

	i = ft_strlen(str);
	if (str[i - 4] != '.' || str[i - 3] != 'c'
		|| str[i - 2] != 'u' || str[i - 1] != 'b')
		return (1);
	return (0);
}
